export interface QueueInfo {
  name: string;
  map: string;
}

/**
 * Get queue information by queue ID
 * @param queueId - The queue ID from the match data
 * @returns Queue information with name and map
 */
export function getQueueInfo(queueId: number): QueueInfo {
  return (
    QUEUE_MAPPINGS[queueId as keyof typeof QUEUE_MAPPINGS] || {
      name: "Unknown Queue",
      map: "Unknown Map",
    }
  );
}

/**
 * Get game mode display name
 * @param gameMode - The game mode from the match data
 * @returns Human-readable game mode name
 */
export function getGameModeDisplayName(gameMode: string): string {
  return (
    GAME_MODE_MAPPINGS[gameMode as keyof typeof GAME_MODE_MAPPINGS] || gameMode
  );
}

/**
 * Get comprehensive match type information
 * Prioritizes queue ID over game mode as it's more specific
 * @param queueId - The queue ID from the match data
 * @returns Comprehensive match information
 */
export function getMatchTypeInfo(queueId: number): {
  displayName: string;
  map: string;
  isRanked: boolean;
  isNormal: boolean;
  isSpecialMode: boolean;
} {
  const queueInfo = getQueueInfo(queueId);

  // Determine match characteristics
  const isRanked = queueInfo.name.toLowerCase().includes("ranked");
  const isNormal =
    !isRanked &&
    (queueInfo.name.toLowerCase().includes("blind") ||
      queueInfo.name.toLowerCase().includes("draft") ||
      queueInfo.name.toLowerCase().includes("normal") ||
      queueInfo.name.toLowerCase().includes("quickplay"));
  const isSpecialMode = !isRanked && !isNormal;

  return {
    displayName: queueInfo.name,
    map: queueInfo.map,
    isRanked,
    isNormal,
    isSpecialMode,
  };
}

